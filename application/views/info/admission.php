      <div class="field">
        <h2>Curriculum</h2>
        <p>The courses under CEP will be grouped into four (4) to lighten up the load of the prospective students, as follows:</p>
        <ul>
          <li><strong>Algorithms and Data Structures</strong></li>
          <li><strong>Discrete Math and Theory of Computation</strong></li>
          <li><strong>Database Systems and Software Engineering</strong></li>
          <li><strong>Computer Networks and Operating Systems</strong></li>
        </ul>
      </div>
      <div class="field">
        <h2>Admission</h2>
        <p>The applicant receives a Certificate of Completion for each course upon successfully passing the exams and other requirements. For those who do not pass the course but have completed the attendance, they will be given Certificate of Attendance. This will not be reflected in the transcript. The courses will be offered in two parts, one after the other. Each part runs for 5 weeks. </p>        
        <ul>
          <li><strong>Part 1: Courses 1 and 2 (5 weeks)</strong>
            <ul class="inner">
              <li>Course 1: Tuesday and Thursday (6-9PM)</li>
              <li>Course 2: Wednesday and Friday (6-9PM)</li>
            </ul>
          </li>            
          <li><strong>Part 2: Courses 3 and 4 (5 weeks)</strong>
            <ul class="inner">
              <li>Course 3: Tuesday and Thursday (6-9PM)</li>
              <li>Course 4: Wednesday and Friday (6-9PM)</li>
            </ul>
          </li>
        </ul>
        <p>If all the relevant courses are passed, he is then eligible to enroll in the MS CS program if Section A requirements are satisfied. The courses can be taken also by those not intending to enroll in the MS CS program. </p>
        <p>Fees for each course will be determined based on the CEP guidelines of UP Cebu.</p>
